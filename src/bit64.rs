/// Calculate division: `n / 10.pow(i)`.
///
/// The dividend is 1-word, 64-bit, same with divisor.
///
/// Return None if: `i > 19`.
pub fn div_single(n: u64, i: usize) -> Option<u64> {
    if i > 19 {
        None
    } else {
        Some(unsafe { unchecked_div_single(n, i) })
    }
}

/// Calculate division: `n / 10.pow(i)`.
///
/// The dividend is 1-word, 64-bit, same with divisor.
///
/// Use the algorithm in Granlund & Montgomery's paper:
///   https://gmplib.org/%7Etege/divcnst-pldi94.pdf
/// A good tutorial for the algorithm:
///   https://homepage.divms.uiowa.edu/%7Ejones/bcd/divide.html
///
/// # Safety:
///
/// It's UB if: `i > 19`.
pub unsafe fn unchecked_div_single(n: u64, i: usize) -> u64 {
    const GM_EXP_MAGICS: [(u64, u32); 20] = [
        (0, 0),
        (0x999999999999999a, 3),
        (0x47ae147ae147ae15, 6),
        (0x0624dd2f1a9fbe77, 9),
        (0xa36e2eb1c432ca58, 13),
        (0x4f8b588e368f0847, 16),
        (0x0c6f7a0b5ed8d36c, 19),
        (0xad7f29abcaf48579, 23),
        (0x5798ee2308c39dfa, 26),
        (0x12e0be826d694b2f, 29),
        (0xb7cdfd9d7bdbab7e, 33),
        (0x5fd7fe17964955fe, 36),
        (0x19799812dea11198, 39),
        (0xc25c268497681c27, 43),
        (0x6849b86a12b9b01f, 46),
        (0x203af9ee756159b3, 49),
        (0xcd2b297d889bc2b7, 53),
        (0x70ef54646d496893, 56),
        (0x2725dd1d243aba0f, 59),
        (0xd83c94fb6d2ac34b, 63),
    ];

    let magic = unsafe { GM_EXP_MAGICS.get_unchecked(i) };

    // (n + ((n * m) >> 64)) >> l
    let high = ((n as u128 * magic.0 as u128) >> 64) as u64;

    // n + high may overflow, so
    // note: magic.1 = l - 1
    (high + ((n - high) >> 1)) >> magic.1
}

/// Calculate division: `n / 10.pow(i)`, return the quotient and remainder.
///
/// The dividend is 2-word, 128-bit, double of divisor.
///
/// Return `None` if: `i > 19` or `n>>64 >= 10.pow(i)`.
pub fn div_double(n: u128, i: usize) -> Option<(u64, u64)> {
    let Some(exp) = POWERS.get(i) else {
        return None;
    };
    if (n >> 64) as u64 >= *exp {
        return None;
    }
    Some(unsafe { unchecked_div_double(n, i) })
}

/// Calculate division: `n / 10.pow(i)`, return the quotient and remainder.
///
/// The dividend is 2-word, 128-bit, double of divisor.
///
/// Use the algorithm in Moller and Granlund's paper: Improved division by
/// invariant integers: https://gmplib.org/~tege/division-paper.pdf
///
/// # Safety:
///
/// It's UB if: `i > 19` or `n>>64 >= 10.pow(i)`.
pub unsafe fn unchecked_div_double(n: u128, i: usize) -> (u64, u64) {
    debug_assert!(i < POWERS.len());
    let exp = unsafe { *POWERS.get_unchecked(i) };

    // check overflow
    debug_assert!((n >> 64) as u64 >= exp);

    // The magics are generated by python code:
    //
    // def gen(d):
    //     zeros = 64 - d.bit_length()
    //     magic = pow(2, 128) // (d << zeros)
    //     magic = magic - pow(2, 64) # make magic fit in 128-bit
    //     return (magic, zeros)
    const MG_EXP_MAGICS: [(u64, u32); 20] = [
        (0, 0),
        (0x9999999999999999, 60),
        (0x47ae147ae147ae14, 57),
        (0x0624dd2f1a9fbe76, 54),
        (0xa36e2eb1c432ca57, 50),
        (0x4f8b588e368f0846, 47),
        (0x0c6f7a0b5ed8d36b, 44),
        (0xad7f29abcaf48578, 40),
        (0x5798ee2308c39df9, 37),
        (0x12e0be826d694b2e, 34),
        (0xb7cdfd9d7bdbab7d, 30),
        (0x5fd7fe17964955fd, 27),
        (0x19799812dea11197, 24),
        (0xc25c268497681c26, 20),
        (0x6849b86a12b9b01e, 17),
        (0x203af9ee756159b2, 14),
        (0xcd2b297d889bc2b6, 10),
        (0x70ef54646d496892, 7),
        (0x2725dd1d243aba0e, 4),
        (0xd83c94fb6d2ac34a, 0),
    ];

    // algorithm:
    //   zn = n << zeros
    //   q = (((magic * zn) >> 64) + zn) >> 64

    // SAFETY: exp has been read by i already
    let &(magic, zeros) = unsafe { MG_EXP_MAGICS.get_unchecked(i) };

    // calc: (high, low) := n << zeros
    let zn = n << zeros;
    let high = (zn >> 64) as u64;
    let low = zn as u64;

    // calc: mul := (magic * zn) >> 64
    let mul_low = (low as u128 * magic as u128) >> 64;
    let mul = (high as u128 * magic as u128) + mul_low;

    // calc: final q
    let q = (mul + zn) >> 64;

    // correction by remainder
    let r = (n - q * exp as u128) as u64;
    if r < exp {
        (q as u64, r)
    } else {
        (q as u64 + 1, r - exp)
    }
}

